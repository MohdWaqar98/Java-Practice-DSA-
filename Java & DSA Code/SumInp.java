import java.util.*;
public class SumInp {
    public static void main(String args[]) {
        Scanner sc =new Scanner(System.in);
        System.out.print("Enter A:");
        int a = sc.nextInt();
        System.out.print("Enter B:");
        int b = sc.nextInt();
        System.out.print("Sum Of A and B:");
        int sum = a+b;
        System.out.print(sum);
    }
}
